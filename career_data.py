def format_career_text(manifest_single: int, manifest_total: int) -> str:
    """
    生成完整的職業分析文字
    包含本命靈數主分析 + 天賦數組成數字的輔助參考
    """
    d = CAREER_DATA.get(manifest_single)
    if not d:
        return "找不到對應的職業資料。"

    # 取出天賦數的組成數字（非零、非本命靈數本身）
    total_str = str(manifest_total)
    component_digits = []
    for ch in total_str:
        n = int(ch)
        if n != 0 and n != manifest_single and n not in component_digits:
            component_digits.append(n)

    # ── 主分析 ──
    text = "💼 適合職業分析\n"
    text += "━━━━━━━━━━━━━━━\n\n"
    text += "你的能量，需要在對的地方才會發光\n\n"
    text += INTRO + "\n\n"
    text += "先來做個簡單的自我檢視。你現在的工作，有沒有這些元素？\n\n"
    for item in d["checklist"]:
        text += f"• {item}\n"
    text += f"\n{d['checklist_end']}\n\n"
    text += "適合的職業方向\n\n"
    text += d["career"] + "\n\n"
    text += "想自己創業的話\n\n"
    text += d["startup"]

    # ── 天賦數輔助 ──
    if component_digits:
        digits_str = " 和 ".join([f"{n}" for n in component_digits])
        text += f"\n\n━━━━━━━━━━━━━━━\n\n"
        text += f"天賦數 {manifest_total} 的輔助參考\n\n"
        text += f"你的天賦數 {manifest_total}，由 {digits_str} 組成。"
        text += "這兩個數字是你達成核心能量的方式——在找工作或創業方向時，可以同時對照以下內容，找出交集，那就是最符合你的順能量方向。\n"

        for n in component_digits:
            cd = CAREER_DATA.get(n)
            if not cd:
                continue
            text += f"\n{n} 號的順能量工作元素\n"
            for item in cd["checklist"]:
                text += f"• {item}\n"
            text += f"\n職業方向參考：{cd['career_short']}\n"
            text += f"創業方向參考：{cd['startup_short']}\n"

        # 交集總結
        all_digits = [manifest_single] + component_digits
        digits_all_str = "、".join([str(n) for n in all_digits])
        text += f"\n━━━━━━━━━━━━━━━\n\n"
        text += f"三個數字的交集，就是你的答案\n\n"
        text += f"把 {digits_all_str} 三個方向放在一起看，找出同時符合這幾個數字的工作元素，符合越多，越是你的順能量方向。"

    return text
