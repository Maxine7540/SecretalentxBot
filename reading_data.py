def get_talent_number_desc(total: int) -> str:
    """根據天賦數生成描述句"""
    digits = [int(d) for d in str(total) if d != '0']
    if len(digits) == 1:
        kw = DIGIT_KEYWORDS.get(digits[0], "")
        return f"天賦數 {total}，核心能量是{kw}——"
    elif len(digits) == 2:
        kw1 = DIGIT_KEYWORDS.get(digits[0], "")
        kw2 = DIGIT_KEYWORDS.get(digits[1], "")
        return f"天賦數 {total}，是{kw1}（{digits[0]}）與{kw2}（{digits[1]}）的結合——"
    else:
        parts = "、".join([f"{DIGIT_KEYWORDS.get(d,'')}（{d}）" for d in digits])
        return f"天賦數 {total}，由{parts}組成——"


def format_outer_reading(manifest: dict, m_meaning: dict) -> str:
    """生成完整的外在性格解析文字"""
    single = manifest["single"]
    total = manifest["total"]
    missing = manifest["missing_numbers"]
    strong = manifest["strong_numbers"]
    grid = manifest["grid"]

    d = LIFE_NUMBER_DESC.get(single, {})
    talent_intro = get_talent_number_desc(total)

    text = "☀️ 外在性格解析（西曆命盤）\n\n"
    text += f"天賦數 {total}　本命靈數 {single}"
    if missing:
        text += f"　空缺：{'、'.join(str(n) for n in missing)}"
    text += "\n\n"

    text += "**你給世界看到的樣子**\n\n"
    text += talent_intro
    text += "你天生就有把想法落地的能力，不是只會空想，也不是埋頭苦幹不懂變通的人。\n\n"
    text += d.get("outer", "") + "\n\n"

    # 圈數說明
    circle_notes = []
    for n in sorted(grid.keys(), key=lambda x: -grid[x]):
        count = grid[n]
        if count >= 2 and n != single:
            kw = DIGIT_KEYWORDS.get(n, "")
            circle_notes.append(f"{n} 有 {count} 圈，帶來{kw}的能量")
    if circle_notes:
        text += "命盤裡" + "；".join(circle_notes) + "。\n\n"

    # 強勢數
    if strong:
        for n, count in strong.items():
            desc = STRONG_NUMBER_DESC.get(n, "")
            if desc:
                text += f"**強勢數 {n}（{count}圈）**\n\n{desc}\n\n"

    # 空缺數
    for n in missing:
        md = MISSING_NUMBER_DESC.get(n, {})
        if md:
            text += f"**空缺 {n}：{md['title']}**\n\n{md['text']}\n\n"

    return text.strip()


